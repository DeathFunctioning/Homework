int ft_find_next_line(char *buffer)
{
	int 	i;
	char	*temp;

	i = 0;
	temp = buffer;
	while(*temp != '\0')
	{
		if (*temp == '\n')
			return (i + 1);
		i++;
	}
	return (0);
}

